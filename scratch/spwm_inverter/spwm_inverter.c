ISR(TIMER1_OVF_vect)
{
    static uint16_t idx = 0;
    uint16_t val = pgm_read_word(&sine_table[idx]);

    if (idx < TABLE_LEN / 2) {
        OCR1A = val;
        OCR1B = 0;
    } else {
        OCR1A = 0;
        OCR1B = val;
    }

    idx++;
    if (idx >= TABLE_LEN)
        idx = 0;
}

